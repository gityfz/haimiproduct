package com.intelligence.business.product.entity;

import java.io.Serializable;
import java.util.Date;

/**
 * @author Zhengyf
 *
 * @date 2017年1月19日
 */
public class Category implements Serializable{

    /**
     * 序列化id
     */
    private static final long serialVersionUID = -2285243022208007276L;

    private Integer id;
    
    private Integer categoryId;
    
    private String categoryName;
 
    private String categoryIndex;
    
    private Integer categoryLevel;
    
    private Integer delFlag;
    
    private Date addTime;

    /**
     * @return the id
     */
    public Integer getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(Integer id) {
        this.id = id;
    }

    /**
     * @return the categoryId
     */
    public Integer getCategoryId() {
        return categoryId;
    }

    /**
     * @param categoryId the categoryId to set
     */
    public void setCategoryId(Integer categoryId) {
        this.categoryId = categoryId;
    }

    /**
     * @return the categoryName
     */
    public String getCategoryName() {
        return categoryName;
    }

    /**
     * @param categoryName the categoryName to set
     */
    public void setCategoryName(String categoryName) {
        this.categoryName = categoryName;
    }

    /**
     * @return the categoryIndex
     */
    public String getCategoryIndex() {
        return categoryIndex;
    }

    /**
     * @param categoryIndex the categoryIndex to set
     */
    public void setCategoryIndex(String categoryIndex) {
        this.categoryIndex = categoryIndex;
    }

    /**
     * @return the categoryLevel
     */
    public Integer getCategoryLevel() {
        return categoryLevel;
    }

    /**
     * @param categoryLevel the categoryLevel to set
     */
    public void setCategoryLevel(Integer categoryLevel) {
        this.categoryLevel = categoryLevel;
    }

    /**
     * @return the delFlag
     */
    public Integer getDelFlag() {
        return delFlag;
    }

    /**
     * @param delFlag the delFlag to set
     */
    public void setDelFlag(Integer delFlag) {
        this.delFlag = delFlag;
    }

    /**
     * @return the addTime
     */
    public Date getAddTime() {
        return addTime;
    }

    /**
     * @param addTime the addTime to set
     */
    public void setAddTime(Date addTime) {
        this.addTime = addTime;
    }

    /**
     * @return the serialversionuid
     */
    public static long getSerialversionuid() {
        return serialVersionUID;
    }

    /* (non-Javadoc)
     * @see java.lang.Object#toString()
     */
    @Override
    public String toString() {
	return "Category [id=" + id + ", categoryId=" + categoryId
		+ ", categoryName=" + categoryName + ", categoryIndex="
		+ categoryIndex + ", categoryLevel=" + categoryLevel
		+ ", delFlag=" + delFlag + ", addTime=" + addTime + "]";
    }
    
    
    

}
