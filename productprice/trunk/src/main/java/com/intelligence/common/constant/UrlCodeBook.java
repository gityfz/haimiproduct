package com.intelligence.common.constant;

/**
 * @author p-sunhao
 *
 */
public enum UrlCodeBook {
	
	// 默认字符串
	URL_CURRENCY_EXCHANGE("http://op.juhe.cn/onebox/exchange/currency");
	
	
	// 字符值
	private String value = null;

    /**
     * 字符串枚举类构造函数
     * 
     * @param id
     * @param message
     */
    UrlCodeBook(String val) {
    	this.value = val;
    }

    /**
     * @return the value
     */
    public String getUrl() {
        return this.value;
    }
    
}
